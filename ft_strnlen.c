#include "libft.h"

size_t	ft_strnlen(const char *s, size_t maxlen)
{
	const char	*i;
	size_t		k;

	i = s;
	k = 0;
	while (k <= maxlen && *s != '\0')
	{
		s++;
	}
	return (s - i);
}
